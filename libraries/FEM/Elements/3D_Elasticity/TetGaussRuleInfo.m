         elseif k>=5 && k<9             
             zeta = [g2,g2,g2,g2];
             zeta(k-4) = 1-3*g2;
             w = w2;
         elseif k>=9 && k<15
             zeta = [g3,g3,g3,g3];
             zeta(jk6(k-8,1)) = 1/2-g3;
             zeta(jk6(k-8,2)) = 1/2-g3;
