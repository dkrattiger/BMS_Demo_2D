        if k<5
            zeta = [g1,g1,g1,g1]; zeta(k) = 1-3*g1;
            w = w1;
        elseif k >=5 && k<9
            zeta = [g2,g2,g2,g2]; zeta(k-4) = 1-3*g2;
            w = w2;
        elseif k>=9
            zeta = [g3,g3,g3,g3];
            zeta(jk6(k-8,1)) = 1/2-g3;
            zeta(jk6(k-8,2)) = 1/2-g3;
            w = 1/6-2*(w1+w2)/3;
        end
    case -14
         g1 = (243-51*sqrt(11)+2*sqrt(16486-9723*sqrt(11)/2))/356;
         g2 = (243-51*sqrt(11)-2*sqrt(16486-9723*sqrt(11)/2))/356;
